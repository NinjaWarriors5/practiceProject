package practiceSelf;

public class PrepSwitch {
	
	public static void main(String[] args) {
		
		int subject = 3;
		switch (subject) {
		case 1: System.out.println("Science");
		break;
		case 2: System.out.println("english");
		break;
		case 3: System.out.println("math");
		break;
		case 4: System.out.println("social");
		break;
		default : System.out.println("art");
		}
	}

}
